export type TranslationKey = keyof (typeof messages)['pt-BR']

export function translate(language: AppLanguage, key: TranslationKey, variables: TranslationVariables = {}) {
  const template = messages[language][key] ?? messages['pt-BR'][key] ?? key

  return Object.entries(variables).reduce(
    (message, [variable, value]) => message.split(`{${variable}}`).join(String(value)),
    template,
  )
}

export function formatMoney(value: number, language: AppLanguage) {
  return new Intl.NumberFormat(languageLocales[language], {
    style: 'currency',
    currency: 'BRL',
  }).format(value)
}

export function formatSignedPercent(value: number, language: AppLanguage) {
  return `${value > 0 ? '+' : ''}${value.toLocaleString(languageLocales[language], {
    maximumFractionDigits: 1,
    minimumFractionDigits: 1,
  })}%`
}
